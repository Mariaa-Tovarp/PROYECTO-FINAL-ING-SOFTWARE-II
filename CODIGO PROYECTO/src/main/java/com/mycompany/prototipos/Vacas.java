    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String a;
       
        
        String columnas[] = {"NOMBRE", "RAZA", "EDAD", "NACIMIENTO", "PERIODO_APARIAMIENTO", "INICIO_DE_LACTANCIA", "PESO"};
        String matriz[][] = new String[listav.size()][7];
       
            for (int i = 0; i < matriz.length; i++) {
            matriz[i][0] = listav.get(i).getNombre();
            matriz[i][1] = listav.get(i).getRaza();
            matriz[i][2] = listav.get(i).getEdad();
            matriz[i][3] = listav.get(i).getFechanac();
            matriz[i][4] = listav.get(i).getPeriapa();
            matriz[i][5] = listav.get(i).getLactan();
            matriz[i][6] = listav.get(i).getPeso();
        }
        
        tabla.setModel(new DefaultTableModel(matriz, columnas));
    }//GEN-LAST:event_jButton2ActionPerformed

