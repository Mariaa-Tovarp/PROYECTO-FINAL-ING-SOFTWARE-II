
package com.mycompany.prototipos;


public class VacasI {
    
    String nombre,raza,edad,fechanac,periapa,lactan,peso;

    public VacasI(String nombre, String raza, String edad, String fechanac, String periapa, String lactan, String peso) {
        this.nombre = nombre;
        this.raza = raza;
        this.edad = edad;
        this.fechanac = fechanac;
        this.periapa = periapa;
        this.lactan = lactan;
        this.peso = peso;
    }

    public String getNombre() {
        return nombre;
    }

    public String getRaza() {
        return raza;
    }

    public String getEdad() {
        return edad;
    }

    public String getFechanac() {
        return fechanac;
    }

    public String getPeriapa() {
        return periapa;
    }

    public String getLactan() {
        return lactan;
    }

    public String getPeso() {
        return peso;
    }


    
    
    
}
